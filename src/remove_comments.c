void remove_comments(char **file)
{
	int i;
	int j;

	i = 0;
	while (file[i])
	{
		j = 0;
		while (file[j])
		{
			if (file[i][j] == '#')
			{
				file[i][j] = '\n';
				file[i][j + 1] = '\0';
				break ;
			}
			j++;
		}
		i++;
	}
}